package week1.day1;

public class LearnOperators {
	public static void main(String[] args) {
		int a = 10;
		int b = 9;
		float c = a/b ;
		System.out.println(a+b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(c);
		
		System.out.println(a==b);	
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a<b && a>b);
		System.out.println(a<b || a>b);
		System.out.println(a !=b);
		
		System.out.println(a++);
		System.out.println(a);
		System.out.println(++a);
		System.out.println(a--);
		System.out.println(a);
	
	
	}

} 
